#!/usr/bin/env node
/**
 * Build a Vercel-ready .env file from .env.local.
 * Run: bun run prepare:vercel
 * Then import .env.vercel.import in Vercel → Settings → Environment Variables.
 */

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const sourcePath = resolve(root, ".env.local");
const outputPath = resolve(root, ".env.vercel.import");

const PROD_CONVEX_URL = "https://mellow-camel-842.eu-west-1.convex.cloud";
const PROD_SITE_URL = "https://malitos.dev";

/** Local-only — must not go to Vercel. */
const SKIP_KEYS = new Set([
  "CONVEX_DEPLOYMENT",
  "NEXT_PUBLIC_CONVEX_SITE_URL",
  "CLOUDINARY_URL",
]);

/** Always use production values on Vercel. */
const OVERRIDES = {
  NEXT_PUBLIC_CONVEX_URL: PROD_CONVEX_URL,
  NEXT_PUBLIC_SITE_URL: PROD_SITE_URL,
};

if (!existsSync(sourcePath)) {
  console.error("Missing .env.local — copy .env.example and fill in values first.");
  process.exit(1);
}

function parseEnvFile(content) {
  const vars = new Map();
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const i = trimmed.indexOf("=");
    if (i === -1) continue;
    const key = trimmed.slice(0, i).trim();
    const value = trimmed.slice(i + 1).trim();
    vars.set(key, value);
  }
  return vars;
}

const vars = parseEnvFile(readFileSync(sourcePath, "utf8"));
const out = new Map();

for (const [key, value] of vars) {
  if (SKIP_KEYS.has(key)) continue;
  out.set(key, value);
}

for (const [key, value] of Object.entries(OVERRIDES)) {
  out.set(key, value);
}

const lines = [
  "# Generated by: bun run prepare:vercel",
  "# Import in Vercel → Settings → Environment Variables → Import .env → Production",
  "# Do not commit this file (contains secrets).",
  "",
];

for (const [key, value] of out) {
  lines.push(`${key}=${value}`);
}

writeFileSync(outputPath, `${lines.join("\n")}\n`, "utf8");

console.log(`Wrote ${outputPath}\n`);
console.log("Skipped (local only):", [...SKIP_KEYS].join(", "));
console.log("Set for production:");
console.log(`  NEXT_PUBLIC_CONVEX_URL=${PROD_CONVEX_URL}`);
console.log(`  NEXT_PUBLIC_SITE_URL=${PROD_SITE_URL}\n`);

if (out.get("ADMIN_SETUP_KEY") === "devmalitos-setup") {
  console.warn(
    "Warning: ADMIN_SETUP_KEY is still the dev default. Change it in .env.local, re-run, and set the same value in Convex Production dashboard.",
  );
}

console.log("Next steps:");
console.log("  1. Vercel → Portfolio project → Settings → Environment Variables");
console.log("  2. Delete old vars if you imported .env.local by mistake");
console.log("  3. Import .env.vercel.import (Production environment)");
console.log("  4. Set ADMIN_SETUP_KEY in Convex Dashboard → Production → Environment Variables");
console.log("  5. Redeploy");
